for(a=1; a<=100; a++){
    console.log(a)
} // 1부터 100까지 출력하는 프로그램.

odd = 1;
while(odd<=100){
    console.log(odd);
    odd += 2;
} // 1~100사이의 홀수를 출력하는 프로그램.

even = 2
while(even<=100){
    console.log(even);
    even += 2;
} // 1~100사이의 짝수를 출력하는 프로그램.


for(x=2; x<10; x++){
    console.log(x + '단');
    for(y=1; y<10; y++){
        console.log(x + "*" + y + "=" + x*y);
    }
} // 구구단 프로그램.
